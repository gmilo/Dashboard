import fs from "node:fs";
import path from "node:path";

function loadEnvFile(filePath) {
  if (!fs.existsSync(filePath)) return;
  const contents = fs.readFileSync(filePath, "utf8");
  for (const rawLine of contents.split("\n")) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) continue;
    const eq = line.indexOf("=");
    if (eq === -1) continue;
    const key = line.slice(0, eq).trim();
    const value = line.slice(eq + 1).trim();
    if (key && !(key in process.env)) process.env[key] = value;
  }
}

function env(key, fallback = "") {
  return process.env[key] ?? fallback;
}

// Ensure we can generate SW from .env.local even before Next loads env.
loadEnvFile(path.resolve(process.cwd(), ".env.local"));
loadEnvFile(path.resolve(process.cwd(), ".env"));

const outPath = path.resolve(process.cwd(), "public/firebase-messaging-sw.js");

const firebaseConfig = {
  apiKey: env("NEXT_PUBLIC_FIREBASE_API_KEY", "REPLACE_ME"),
  authDomain: env("NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN", "REPLACE_ME"),
  projectId: env("NEXT_PUBLIC_FIREBASE_PROJECT_ID", "REPLACE_ME"),
  storageBucket: env("NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET", "REPLACE_ME"),
  messagingSenderId: env("NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID", "REPLACE_ME"),
  appId: env("NEXT_PUBLIC_FIREBASE_APP_ID", "REPLACE_ME")
};

const contents = `/* eslint-disable no-undef */
// AUTO-GENERATED by scripts/generate-firebase-sw.mjs
// Used for Firebase Cloud Messaging background notifications.
importScripts("https://www.gstatic.com/firebasejs/9.22.0/firebase-app-compat.js");
importScripts("https://www.gstatic.com/firebasejs/9.22.0/firebase-messaging-compat.js");

firebase.initializeApp(${JSON.stringify(firebaseConfig, null, 2)});
firebase.messaging();
`;

fs.writeFileSync(outPath, contents, "utf8");
